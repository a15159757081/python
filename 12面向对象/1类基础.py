class Student:
    name = None
    gender = None
    nationality = None
    native_place = None
    age = None


# 设计一个类（类比生活中设计一张登记表）

stu_1 = Student()
# 创建对象（类比生活打印一张登记表）

stu_1.name = "周杰伦"
stu_1.gender = "男"
stu_1.nationality = "中国"
stu_1.native_place = "台湾"
stu_1.age = 31
# 对象属性进行赋值

print(stu_1.name)
print(stu_1.gender)
print(stu_1.nationality)
print(stu_1.native_place)
print(stu_1.age)